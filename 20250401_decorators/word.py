
#  1              год
#  2,3,4,24,104   года
#  5,11-20,37,111 лет
#             1, 2-4, 5
def forms(N, onemany):
    d10 = N % 10
    d100 = N % 100
    if 4 < d100 < 21:  # 2, 3, 4, 5, ..., 10, 11, ...., 19, 20
        return onemany[2]  # лет
    if d10 == 1:
        return onemany[0]  # 1, 21, 101     ... год
    return onemany[1]      # 2,3,4, 24, 104 ... года


if __name__ == '__main__':
    print(forms(104, ('год', 'года', 'лет')))